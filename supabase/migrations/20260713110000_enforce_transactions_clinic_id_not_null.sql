-- =============================================================================
-- منع تكرار مشكلة clinic_id = NULL في transactions نهائياً
-- =============================================================================
-- السبب الجذري المكتشف: عدة نقاط إدراج (صرف رواتب مساعدين، مصاريف أطباء،
-- رواتب موظفين، شحن رصيد) كانت — في نسخ سابقة من الكود أو بسبب أعطال مؤقتة —
-- تُدخل حركات بدون clinic_id. بما إن قيد التفرّد transactions_reference_unique
-- على (clinic_id, reference_type, reference_id) لا يعمل بشكل صحيح لو
-- clinic_id = NULL (القيم الفارغة لا تتساوى ببعضها بمنطق SQL)، فحص منع
-- التكرار بالكود كان يفشل بصمت ويسمح بإدخال حركات مكرّرة أيضاً.
--
-- كل البيانات القديمة صُحّحت يدوياً (سكربتات 60-73). هذا القيد يمنع تكرار
-- المشكلة بشكل قاطع من الآن فصاعداً — أي محاولة إدخال حركة بدون clinic_id
-- سترفضها قاعدة البيانات فوراً برسالة خطأ واضحة، بدل تسرّب بيانات فاسدة.
-- =============================================================================

ALTER TABLE public.transactions
  ALTER COLUMN clinic_id SET NOT NULL;
